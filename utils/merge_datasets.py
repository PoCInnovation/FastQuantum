import json
import argparse
import os

def merge_datasets(input_paths, output_path):
    """
    Merges multiple JSON dataset files into a single JSON file.

    Args:
        input_paths (list): A list of file paths to the input JSON datasets.
        output_path (str): The file path for the merged output JSON dataset.
    """
    merged_data = []
    print(f"Starting merge of {len(input_paths)} datasets...")

    for i, path in enumerate(input_paths):
        if not os.path.exists(path):
            print(f"Warning: Input file not found, skipping: {path}")
            continue
        try:
            with open(path, 'r') as f:
                data = json.load(f)
                if isinstance(data, list):
                    merged_data.extend(data)
                    print(f" - Added {len(data)} samples from {os.path.basename(path)}")
                else:
                    print(f" - Skipping {os.path.basename(path)}: Expected a list of samples, got {type(data)}")
        except json.JSONDecodeError:
            print(f" - Skipping {os.path.basename(path)}: Invalid JSON format.")
        except Exception as e:
            print(f" - An error occurred while reading {os.path.basename(path)}: {e}")

    if not merged_data:
        print("No data was merged. Output file will be empty or not created.")
        return

    # Assign new sequential IDs to merged data
    for i, sample in enumerate(merged_data):
        sample['id'] = i

    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, 'w') as f:
        json.dump(merged_data, f, indent=2)

    print(f"\nSuccessfully merged {len(merged_data)} total samples into {output_path}")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Merge multiple JSON dataset files generated by generate_v1_dataset.py into a single file."
    )
    parser.add_argument(
        "--inputs",
        nargs='+',  # Accepts one or more file paths
        required=True,
        help="Space-separated list of input JSON dataset file paths (e.g., 'Dataset/maxcut.json Dataset/mis.json')"
    )
    parser.add_argument(
        "--output",
        type=str,
        default="Dataset/merged_qaoa_dataset.json",
        help="Path for the merged output JSON dataset file."
    )
    args = parser.parse_args()

    merge_datasets(args.inputs, args.output)